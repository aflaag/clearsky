"""
Star Removal (SR) Dataset Builder

Creates (synthetic_input_with_stars, starless) training pairs for DDPM training.
It loads synthetic NPY inputs (generated by inject_stars.py) and pairs them with 
the corresponding starless TIFF ground truths (produced by StarNet2). It uses an 
integral image to efficiently sample valid patches strictly within the provided 
pixel masks, discarding patches that are completely black or featureless.
"""

import argparse
from pathlib import Path

import numpy as np
from PIL import Image
import tifffile


def build_candidate_mask(pixel_mask, patch_size):
    """Integral image (summed area table) on the pixel mask.

    Returns a boolean map (H, W) of valid top-left positions for patches.
    """
    H, W = pixel_mask.shape
    p = patch_size

    if H < p or W < p:
        return np.zeros((H, W), dtype=bool)

    pv = pixel_mask.astype(np.int32)
    sat = np.zeros((H + 1, W + 1), dtype=np.int64)
    sat[1:, 1:] = np.cumsum(np.cumsum(pv, axis=0), axis=1)

    y_max = H - p
    x_max = W - p

    Y, X = np.meshgrid(np.arange(y_max), np.arange(x_max), indexing="ij")

    patch_sums = (
        sat[Y + p, X + p]
        - sat[Y, X + p]
        - sat[Y + p, X]
        + sat[Y, X]
    )

    candidate_mask = np.zeros((H, W), dtype=bool)
    candidate_mask[:y_max, :x_max] = patch_sums == p * p
    return candidate_mask


def sample_position(candidate_mask):
    """Samples a random top-left position among the valid ones."""
    ys, xs = np.where(candidate_mask)
    if len(ys) == 0:
        return None, None
    idx = np.random.randint(len(ys))
    return int(ys[idx]), int(xs[idx])


def apply_augmentation(patch, flip_h, flip_v, rot_k):
    """Applies deterministic augmentation given the parameter set."""
    if flip_h:
        patch = np.fliplr(patch)
    if flip_v:
        patch = np.flipud(patch)
    if rot_k > 0:
        patch = np.rot90(patch, k=rot_k)
    return np.ascontiguousarray(patch)


def random_aug_params():
    """Samples random augmentation parameters."""
    return {
        "flip_h": np.random.rand() < 0.5,
        "flip_v": np.random.rand() < 0.5,
        "rot_k": int(np.random.randint(4)),
    }


def save_patch(patch, stem, npy_dir, png_dir):
    """Saves patch as float32 NPY and, if png_dir is not None, also uint8 PNG."""
    np.save(npy_dir / f"{stem}.npy", patch.astype(np.float32))
    if png_dir is not None:
        png = np.clip(patch * 255.0, 0, 255).astype(np.uint8)
        Image.fromarray(png, mode="RGB").save(png_dir / f"{stem}.png")


def load_starless_tiff(tiff_path):
    """Loads a starless TIFF produced by StarNet2.

    StarNet2 saves 16-bit TIFFs → normalizes to [0, 1] by dividing by 65535.
    """
    arr = tifffile.imread(tiff_path)

    if arr.dtype == np.uint16:
        return arr.astype(np.float32) / 65535.0
    elif arr.dtype == np.uint8:
        return arr.astype(np.float32) / 255.0
    else:
        raise ValueError(f"Unexpected dtype for starless TIFF: {arr.dtype}")


def process_file(
    npy_path,
    starless_path,
    mask_path,
    input_npy_dir,
    input_png_dir,
    target_npy_dir,
    target_png_dir,
    patch_size,
    crops_per_image,
):
    print(f"Processing {npy_path.name}")

    # --- Load input image (synthetic with stars) ---
    synthetic = np.load(npy_path)  # (H, W, 3) float32
    if synthetic.ndim != 3 or synthetic.shape[-1] != 3:
        print(f"  [SKIP] unexpected synthetic NPY shape: {synthetic.shape}")
        return 0

    H, W, _ = synthetic.shape

    if H < patch_size or W < patch_size:
        print(
            f"  [SKIP] {H}x{W} too small for {patch_size}x{patch_size} patch"
        )
        return 0

    # --- Load starless (ground truth) ---
    if not starless_path.exists():
        print(f"  [SKIP] starless not found: {starless_path}")
        return 0

    starless = load_starless_tiff(starless_path)

    if starless.ndim == 2:
        # If grayscale (1 channel), duplicate it to 3 RGB channels
        starless = np.stack([starless, starless, starless], axis=-1)
    elif starless.ndim == 3 and starless.shape[-1] == 4:
        # If it has an Alpha channel (4 channels), remove it keeping only RGB
        starless = starless[:, :, :3]

    # Now the safety check can be absolute
    if starless.shape != synthetic.shape:
        print(f"  [SKIP] Unrecoverable mismatch: starless {starless.shape} vs synthetic input {synthetic.shape}")
        return 0

    # --- Load mask ---
    if not mask_path.exists():
        print(f"  [SKIP] mask not found: {mask_path}")
        return 0

    pixel_mask = np.load(mask_path)  # (H, W) bool
    if pixel_mask.shape != (H, W):
        print(f"  [SKIP] mask shape {pixel_mask.shape} != {H}x{W}")
        return 0

    # --- Calculate valid positions ---
    candidate_mask = build_candidate_mask(pixel_mask, patch_size)
    n_candidates = int(candidate_mask.sum())

    if n_candidates == 0:
        print(
            f"  [SKIP] no valid position for {patch_size}x{patch_size} patch"
        )
        return 0

    png_label = " + png" if input_png_dir is not None else ""
    print(
        f"  Shape: {H}x{W} | Candidates: {n_candidates} | Crops: {crops_per_image}{png_label}"
    )

    basename = npy_path.stem
    saved = 0
    
    # Thresholds
    THRESH_BLACK = 0.05
    THRESH_VAR = 1e-5
    MAX_RETRIES = 10  # Avoid infinite loops if the image is entirely dark

    for i in range(crops_per_image):
        valid_patch_found = False
        
        # Try up to MAX_RETRIES times to find an "interesting" patch
        for attempt in range(MAX_RETRIES):
            y, x = sample_position(candidate_mask)
            if y is None:
                break  # No valid position in the entire mask
            
            # Temporarily extract only the input to evaluate it
            patch_input = synthetic[y : y + patch_size, x : x + patch_size, :]
            
            patch_mean = float(patch_input.mean())
            patch_var = float(patch_input.var())
            
            # Validity condition: not too black AND not too flat
            if patch_mean >= THRESH_BLACK and patch_var >= THRESH_VAR:
                valid_patch_found = True
                break
        
        if not valid_patch_found:
            print(f"  [WARN] Impossible to find a non-empty patch for crop {i} after {MAX_RETRIES} attempts")
            continue

        # If we are here, we have optimal (y, x) coordinates! Proceed with target
        patch_target = starless[y : y + patch_size, x : x + patch_size, :]

        # Same augmentation for both
        aug = random_aug_params()
        patch_input = apply_augmentation(patch_input, **aug)
        patch_target = apply_augmentation(patch_target, **aug)

        stem = f"{basename}_{i:04d}"
        save_patch(patch_input, stem, input_npy_dir, input_png_dir)
        save_patch(patch_target, stem, target_npy_dir, target_png_dir)
        saved += 1

    print(f"  Saved {saved}/{crops_per_image} crops")
    return saved


def main():
    parser = argparse.ArgumentParser(
        description="Creates (synthetic_input_with_stars, starless) pairs for DDPM training."
    )
    parser.add_argument(
        "--npy-dir", 
        default="assets/outputs-npy-synthetic", 
        help="NPY with synthetic stars generated by inject_stars.py"
    )
    parser.add_argument(
        "--starless-dir",
        default="assets/outputs-starless",
        help="Starless TIFFs from StarNet2",
    )
    parser.add_argument(
        "--mask-dir",
        default="assets/outputs-mask",
        help="Masks from make_masks.py",
    )
    parser.add_argument(
        "--out-dir",
        default="dataset_sr",
        help="Output folder (default: dataset_sr)",
    )
    parser.add_argument(
        "--patch-size",
        type=int,
        default=256,
        help="Patch size (default: 256)",
    )
    parser.add_argument(
        "--crops-per-image",
        type=int,
        default=50,
        help="Crops per image (default: 50)",
    )
    parser.add_argument(
        "--seed", type=int, default=42, help="Seed (default: 42)"
    )
    parser.add_argument(
        "--save-png",
        action="store_true",
        help="Also save PNG patches (useful for visual debugging)",
    )
    args = parser.parse_args()

    np.random.seed(args.seed)

    npy_dir = Path(args.npy_dir)
    starless_dir = Path(args.starless_dir)
    mask_dir = Path(args.mask_dir)
    out = Path(args.out_dir)

    input_npy_dir = out / "input" / "npy"
    target_npy_dir = out / "target" / "npy"

    # PNG folders created only if requested
    input_png_dir = out / "input" / "png" if args.save_png else None
    target_png_dir = out / "target" / "png" if args.save_png else None

    for d in [input_npy_dir, target_npy_dir]:
        d.mkdir(parents=True, exist_ok=True)

    if args.save_png:
        input_png_dir.mkdir(parents=True, exist_ok=True)
        target_png_dir.mkdir(parents=True, exist_ok=True)

    npy_files = sorted(npy_dir.glob("*.npy"))
    if not npy_files:
        print(f"No .npy files found in {npy_dir}")
        return

    print(f"Found {len(npy_files)} NPY files (Synthetic)")
    print(f"Patch size : {args.patch_size}x{args.patch_size}")
    print(f"Crops/image: {args.crops_per_image}")
    print(f"Save PNG   : {args.save_png}")
    print(f"Output     : {out}\n")

    total = 0
    for npy_file in npy_files:
        stem = npy_file.stem
        total += process_file(
            npy_path=npy_file,
            starless_path=starless_dir / f"{stem}.tif",  # TIFF maintained
            mask_path=mask_dir / f"{stem}.npy",
            input_npy_dir=input_npy_dir,
            input_png_dir=input_png_dir,
            target_npy_dir=target_npy_dir,
            target_png_dir=target_png_dir,
            patch_size=args.patch_size,
            crops_per_image=args.crops_per_image,
        )

    print(f"\nCompleted. Total pairs saved: {total}")


if __name__ == "__main__":
    main()
